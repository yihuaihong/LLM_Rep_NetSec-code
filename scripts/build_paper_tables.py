# Table: V4 zero-shot transfer
# ============================================================
def table_zero_shot():
    d = load("phase2_zero_shot_transfer.json")
    if d is None:
        print("  skip zero-shot (not done yet)"); return
    out = ["% V4 zero-shot transfer auto"]
    out.append("\\begin{tabular}{lcccccc}")
    out.append("\\toprule")
    out.append(" & \\multicolumn{2}{c}{CIC-2018 (same schema)} & "
               "\\multicolumn{2}{c}{IoT-23} & \\multicolumn{2}{c}{CTU-13} \\\\")
    out.append("\\cmidrule(lr){2-3}\\cmidrule(lr){4-5}\\cmidrule(lr){6-7}")
    out.append("Model & in-dom & cic2017$\\to$ & in-dom & cic2017$\\to$ & in-dom & cic2017$\\to$ \\\\")
    out.append("\\midrule")
    for model, dd in d.items():
        cells = [model.replace("_", "\\_")]
        for ds in ["cicids2018", "iot23", "ctu13"]:
            if ds not in dd: continue
            r = dd[ds]
            in_dom = max(v["in_domain_probe_auroc"] for v in r["per_layer"].values())
            best_t = max(v["cicids2017_dir_auroc"] for v in r["per_layer"].values())
            cells.append(fmt(in_dom))
            cells.append(fmt(best_t))
        out.append(" & ".join(cells) + " \\\\")
    out.append("\\bottomrule")
    out.append("\\end{tabular}")
    write_table("auto_zero_shot.tex", "\n".join(out) + "\n")


