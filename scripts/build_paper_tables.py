"""Convert JSON metric files into LaTeX tables auto-included in the paper.

Reads:
  results/metrics/<model>_<dataset>.json    — probe accuracy
  results/metrics/phase1_*.json             — Phase 1 experiments
  results/metrics/phase2_*.json             — Phase 2 robustness checks

Writes:
  paper/tables/auto_*.tex   — \input-able LaTeX tables that match the paper
"""
import json, os, sys
from pathlib import Path

BASE = Path(__file__).resolve().parent.parent
METRICS = BASE / "results" / "metrics"
OUT = BASE / "paper" / "tables"
OUT.mkdir(parents=True, exist_ok=True)


def load(name):
    p = METRICS / name
    return json.load(open(p)) if p.exists() else None


def fmt(v, prec=3):
    if v is None: return "—"
    if isinstance(v, float):
        return f"{v:.{prec}f}"
    return str(v)


def write_table(filename, body):
    (OUT / filename).write_text(body)
    print(f"  wrote {filename} ({len(body)} chars)")


# ============================================================
# Table 1: per-model probe accuracy (in-domain)
# ============================================================
def table_probe_acc():
    rows = []
    for model in ["Llama-3.1-8B-Instruct", "Mistral-7B-Instruct-v0.3",
                  "Qwen3-8B", "gemma-2-9b-it"]:
        for ds in ["cicids2017", "unsw_nb15"]:
            d = load(f"{model}_{ds}.json")
            if d is None: continue
            rows.append({
                "model": model.replace("_", "\\_"),
                "ds": ds,
                "best_layer": d.get("best_layer"),
                "known_acc": d.get("known_accuracy"),
                "known_auroc": d.get("known_auroc"),
                "holdout_acc": d.get("holdout_accuracy"),
                "holdout_auroc": d.get("holdout_auroc"),
                "gap_acc": d.get("known_accuracy", 0) - d.get("holdout_accuracy", 0),
            })

    out = ["% auto-generated by scripts/build_paper_tables.py"]
    out.append("\\begin{tabular}{lccccc}")
    out.append("\\toprule")
    out.append("Model & Dataset & best L & known acc & holdout acc & gap \\\\")
    out.append("\\midrule")
    for r in rows:
        ds_short = "CIC-2017" if r["ds"] == "cicids2017" else "UNSW"
        out.append(f"{r['model'][:28]} & {ds_short} & {r['best_layer']} & "
                   f"{fmt(r['known_acc'])} & {fmt(r['holdout_acc'])} & "
                   f"{r['gap_acc']:+.3f} \\\\")
    out.append("\\bottomrule")
    out.append("\\end{tabular}")
    write_table("auto_probe_acc.tex", "\n".join(out) + "\n")


