# Table: V1 TF-IDF baseline
# ============================================================
def table_tfidf():
    d = load("phase1_expC_tfidf_baseline.json")
    if d is None:
        print("  skip TF-IDF (not done yet)"); return
    out = ["% TF-IDF baseline auto"]
    out.append("\\begin{tabular}{lcccccc}")
    out.append("\\toprule")
    out.append("Vectorizer & \\multicolumn{3}{c}{CIC-IDS2017} & \\multicolumn{3}{c}{UNSW-NB15} \\\\")
    out.append("\\cmidrule(lr){2-4}\\cmidrule(lr){5-7}")
    out.append("           & known & holdout & gap & known & holdout & gap \\\\")
    out.append("\\midrule")
    for vec in ["word_unigrams_bigrams", "char_3to5"]:
        c = d["cicids2017"][vec]
        u = d["unsw_nb15"][vec]
        nice = "word $1$--$2$g" if vec == "word_unigrams_bigrams" else "char $3$--$5$g"
        out.append(f"TF-IDF {nice} & "
                   f"{fmt(c['known_accuracy'])} & {fmt(c['holdout_accuracy'])} & "
                   f"{c['gap_acc']:+.3f} & "
                   f"{fmt(u['known_accuracy'])} & {fmt(u['holdout_accuracy'])} & "
                   f"{u['gap_acc']:+.3f} \\\\")
    out.append("\\bottomrule")
    out.append("\\end{tabular}")
    write_table("auto_tfidf.tex", "\n".join(out) + "\n")


