# Table: V2 sparse decomposition R^2
# ============================================================
def table_sparse():
    d = load("phase2_b1_sparse_decomp.json")
    if d is None:
        print("  skip sparse decomp (not done yet)"); return
    out = ["% V2 sparse decomp R^2 auto"]
    out.append("\\begin{tabular}{llcccc}")
    out.append("\\toprule")
    out.append("Model & Dataset & $R^2$(all) & $R^2$($k{=}1$) & $R^2$($k{=}3$) & top-1 axis \\\\")
    out.append("\\midrule")
    for m in d:
        for ds, r in d[m].items():
            keys = list(r["greedy_topk"].keys())
            k1 = "1" if "1" in keys else 1
            k3 = "3" if "3" in keys else 3
            top1 = r["greedy_topk"][k1]["features"][0].replace("_", "\\_")
            out.append(f"{m.replace('_', chr(92)+'_')} & {ds.replace('_', chr(92)+'_')} & "
                       f"{fmt(r['r2_with_all'])} & {fmt(r['greedy_topk'][k1]['r2'])} & "
                       f"{fmt(r['greedy_topk'][k3]['r2'])} & \\texttt{{{top1[:25]}}} \\\\")
    out.append("\\bottomrule")
    out.append("\\end{tabular}")
    write_table("auto_sparse.tex", "\n".join(out) + "\n")


