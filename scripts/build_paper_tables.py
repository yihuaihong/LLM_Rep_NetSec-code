# ============================================================
# Table: A2 multi-probe
# ============================================================
def table_multi_probe():
    d = load("phase2_a2_multi_probe.json")
    if d is None:
        print("  skip multi-probe (not done yet)"); return
    out = ["% Multi-probe auto"]
    out.append("\\begin{tabular}{llcccc}")
    out.append("\\toprule")
    out.append("Model & Dataset & LR gap & LDA gap & PCA-LR gap & MLP gap \\\\")
    out.append("\\midrule")
    for m in d:
        for ds, dd in d[m].items():
            row_cells = [m.replace("_", "\\_"), ds.replace("_", "\\_")]
            for p in ["lr", "lda", "pca_lr", "mlp"]:
                if p in dd:
                    row_cells.append(f"{dd[p]['gap_acc']:+.3f}")
                else:
                    row_cells.append("—")
            out.append(" & ".join(row_cells) + " \\\\")
    out.append("\\bottomrule")
    out.append("\\end{tabular}")
    write_table("auto_multi_probe.tex", "\n".join(out) + "\n")


def main():
    print("Building paper tables from results/metrics/ ...")
    table_probe_acc()
    table_tfidf()
    table_zero_shot()
    table_sparse()
    table_multi_seed()
    table_multi_probe()
    print(f"\nAll tables written to {OUT}/")


if __name__ == "__main__":
    main()
